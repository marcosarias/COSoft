    private String darFormato(String s){
    
        String[] a = s.split("-");
            
        while(a[0].length() < 4)
            a[0] = "0" + a[0];
        
        while(a[1].length() < 8)
            a[1] = "0" + a[1];
            
        return a[0] + "-" + a[1];
        
    }
    
