    private void llenarTodo() {
        
        materiales = new ArrayList<>();
        
        ControladorMaterial.getMateriales(materiales);
        
        for(Material material : materiales){
        
            Object[] data = { material.getTipo() + " " + material.getMarca(), 0 };
            modelo.addRow(data);
            
        
        }
        
    }
