    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        TipoCuota cuotaSeleccionada = tipoCuotas.get(jComboBox1.getSelectedIndex());
        String resultado = ControladorFactura.generarCuotas(cuotaSeleccionada.getIdCuota(), Fecha.invertirFecha(jTextField1.getText()), jComboBox2.getSelectedIndex() + 1, (int)jSpinner1.getValue(), cuotaSeleccionada.getImporte());
        if(resultado.equals("")){  //No hubo error
            Mensaje.mostrarMensaje(rootPane, "Cuotas generadas con exito", "Enhorabuena", JOptionPane.INFORMATION_MESSAGE);
            dispose();
        }
        else Mensaje.mostrarMensaje(rootPane, "Error al generar las cuotas:\n" + resultado, "Error", JOptionPane.ERROR_MESSAGE);

    }//GEN-LAST:event_jButton3ActionPerformed

