    private void llenarTodo() {
        
        for(int i = jTable1.getRowCount() - 1; i >= 0; i--){
        
            modelo.removeRow(i);
        
        }
        
        materiales.clear();
        
        ControladorFactura.getMaterialesFactura(materiales, nroFactura);
        
        float total = 0;
        for(Material material : materiales){
        
            float sub = material.getCantidadCompra()*material.getPrecio();
            total += sub;
            Object[] data = { material.getTipo(), material.getMarca(), material.getCantidadCompra(), sub };
            modelo.addRow(data);
        
        }
        
        jTextField2.setText("$ " + total);
        
    }
