    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(tipo == 0){ //Nuevo
            
            profesional = new Profesional();
            profesional.setIdlocalidad(localidades.get(jComboBox2.getSelectedIndex()).getIdlocalidad());
            profesional.setMatricula((int) Double.parseDouble(jTextField1.getText()));
            profesional.setNombre(jTextField11.getText());
            profesional.setTelefonos(jTextField10.getText());
            profesional.setDireccion(jTextField9.getText());
            profesional.setCbu(jTextField12.getText());
            profesional.setBanco(jTextField13.getText());
            if(jCheckBox1.isSelected())
                profesional.setActivo(1);
            else profesional.setActivo(0);
            //Tomo valores de campos y se los asigno al objeto
            String resultado = ControladorProfesional.insertar(profesional);
            if(resultado.equals(""))  //No hubo error
                Mensaje.mostrarMensaje(rootPane, "Profesional agregado con √©xito", "Enhorabuena", JOptionPane.INFORMATION_MESSAGE);
            else Mensaje.mostrarMensaje(rootPane, "Error al agregar profesional:\n" + resultado, "Error", JOptionPane.ERROR_MESSAGE);
            
            dispose();
            
        }
        else {
        
            profesional.setIdlocalidad(localidades.get(jComboBox2.getSelectedIndex()).getIdlocalidad());
            profesional.setNombre(jTextField11.getText());
            profesional.setTelefonos(jTextField10.getText());
            profesional.setDireccion(jTextField9.getText());
            profesional.setCbu(jTextField12.getText());
            profesional.setBanco(jTextField13.getText());
            if(jCheckBox1.isSelected())
                profesional.setActivo(1);
            else profesional.setActivo(0);
            String resultado = ControladorProfesional.editar(profesional);
            if(resultado.equals(""))  //No hubo error
                Mensaje.mostrarMensaje(rootPane, "Profesional editado con √©xito", "Enhorabuena", JOptionPane.INFORMATION_MESSAGE);
            else Mensaje.mostrarMensaje(rootPane, "Error al editar profesional:\n" + resultado, "Error", JOptionPane.ERROR_MESSAGE);
            
            dispose();
            
        }
    }//GEN-LAST:event_jButton3ActionPerformed

