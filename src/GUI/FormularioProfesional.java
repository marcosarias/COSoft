    private void llenarLocalidades(){
    
        localidades = new ArrayList<>();
        ControladorLocalidad.getLocalidades(localidades);
        
        for(Localidad localidad : localidades){
        
            modeloLocalidades.addElement(localidad.getNombre());
        
        }
    
    }
    
    public void setData(int id){
        
        this.id = id;
        profesional = new Profesional();
        profesional.setMatricula(id);
        ControladorProfesional.getDatos(profesional);
        
        Boolean encontro = false;
        int i = 0;
        while(!encontro){
        
            if(localidades.get(i).getIdlocalidad() == profesional.getIdlocalidad()){
            
                encontro = true;
                jComboBox2.setSelectedIndex(i);
            
            }
            i++;
        
        }
        
        jTextField11.setText(profesional.getNombre());
        jTextField10.setText(profesional.getTelefonos());
        jTextField9.setText(profesional.getDireccion());
        jTextField13.setText(profesional.getBanco());
        jTextField12.setText(profesional.getCbu());
        jTextField1.setText(String.valueOf(profesional.getMatricula()));
        if(profesional.getActivo() == 1)
            jCheckBox1.setSelected(true);
        else jCheckBox1.setSelected(false);
            
                
    }
    
